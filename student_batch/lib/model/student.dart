class Student {
  String? studentId;
  String? fname;
  String? lname;
  int? age;
  String? username;
  String? password;
  String? batchId;

  Student({
    required this.studentId,
    required this.fname,
    required this.lname,
    required this.age,
    required this.username,
    required this.password,
    required this.batchId,
  });
}
